    private void openDatabaseConnection() {
        try {
            String url = "jdbc:mysql://localhost:3306/ewallet";
            String user = "root";
            String pass = "admin";

            connection = DriverManager.getConnection(url, user, pass);
            System.out.println("Database connected successfully!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error connecting to database: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    


    
    private void formWindowClosing(java.awt.event.WindowEvent evt) {                                    
        try {
            if (connection != null) {
                connection.close();
                System.out.println("Database connection closed.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error closing database connection: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   
